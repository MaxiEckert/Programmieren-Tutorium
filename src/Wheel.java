
public class Wheel {

	short rimDiameter;
	
	short tireWidth;
	
	short tireDiameter;
	
	String modelNumber;

	public Wheel(short rimDiameter, short tireWidth, short tireDiameter, String modelNumber) {
		this.rimDiameter = rimDiameter;
		this.tireWidth = tireWidth;
		this.tireDiameter = tireDiameter;
		this.modelNumber = modelNumber;
	}
	
}
